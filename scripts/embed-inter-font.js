/**
 * Embeds src/fonts/Inter-Regular.ttf as base64 into src/editor/embeddedInterFont.js
 * so the editor can use Inter by default without loading from the network.
 * Run: node scripts/embed-inter-font.js
 */
const fs = require('fs');
const path = require('path');

const repoRoot = path.resolve(__dirname, '..');
const fontPath = path.join(repoRoot, 'src', 'fonts', 'Inter-Regular.ttf');
const outPath = path.join(repoRoot, 'src', 'editor', 'embeddedInterFont.js');

const buffer = fs.readFileSync(fontPath);
const base64 = buffer.toString('base64');

const content = `/**
 * Embedded Inter-Regular font (base64) for default editor styling.
 * Generated by scripts/embed-inter-font.js â€” do not edit manually.
 */
export const INTER_REGULAR_BASE64 = "${base64}";
`;

fs.writeFileSync(outPath, content, 'utf8');
console.log('Wrote', outPath);
console.log('Base64 length:', base64.length);
